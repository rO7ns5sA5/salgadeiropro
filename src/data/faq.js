// Busca simples offline por palavras-chave
export function buscarFaq(pergunta) {
  const q = pergunta.toLowerCase()
  const termos = q.split(/\s+/).filter(t => t.length > 3)

  const scored = faqCompacta.map(item => {
    const texto = (item.pergunta + ' ' + item.resposta).toLowerCase()
    const score = termos.reduce((acc, t) => acc + (texto.includes(t) ? 1 : 0), 0)
    return { ...item, score }
  })

  return scored
    .filter(i => i.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 3)
}
